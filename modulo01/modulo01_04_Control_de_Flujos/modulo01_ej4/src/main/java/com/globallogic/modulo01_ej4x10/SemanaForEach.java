package com.globallogic.modulo01_ej4x10;

public class SemanaForEach {

	public static void main(String[] args) {
		String dias[] = {"Lunes","Martes","Miercoles","Jueves","Viernes",
				   "Sabado","Domingo"};
		
		for (String dia : dias) {
			System.out.println("Día de la semana " + dia );
		}
	}

}
